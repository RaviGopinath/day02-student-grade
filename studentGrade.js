let gopi;

gopi = 80;

if(gopi >= 90){
    alert("Gopi is S grade");
}
else if(gopi >= 80){
    alert("Gopi is A grade");
}
else if(gopi >= 70){
    alert("Gopi is B grade");
}
else if(gopi >= 60){
    alert("Gopi is C grade");
}
else if(gopi >= 50){
    alert("Gopi is D grade");
}
else{
    alert("Gopi is Fail");
}